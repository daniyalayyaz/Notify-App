class RequestUsers {
  final String name;
  final String email;
  final String phoneNo;
  final String designation;
  final String age;
  final String owner;
  final String address;
  final String fname;
  final String fphoneNo;
  final String uid;

  RequestUsers({
    required this.name,
    required this.email,
    required this.phoneNo,
    required this.designation,
    required this.age,
    required this.owner,
    required this.address,
    required this.fname,
    required this.fphoneNo,
    required this.uid,
  });
}
